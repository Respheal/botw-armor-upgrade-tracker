const NumberButton = {
  name: 'NumberButton',
  template: '#number-button',
  inheritAttrs: false,
  props: ['modelValue'],
  emits: ['update:modelValue'],
  methods: {
    clamp: function (value) {
      value = value || 0;
      value = parseInt(value);
      if (!value || typeof(value) !== 'number' || isNaN(value)) {
        value = 0;
      }
      value = Math.round(value);
      value = Math.min(999, value);
      value = Math.max(0, value);
      return value;
    },
    emit: function (increment) {
      const value = this.clamp(this.modelValue + increment);
      this.$emit('update:modelValue', value);
    }
  },
  computed: {
    value: {
      get: function () {
        return this.modelValue;
      },
      set: function (value) {
        value = this.clamp(value);
        this.$emit('update:modelValue', value);
      }
    }
  }
};

document.body.addEventListener('keyup', function (evt) {
  if (app.showObtained !== 'speedrun100') {
    return;
  }

  const space = evt.keyCode === 32;
  const escape = evt.keyCode === 27;
  const ctrl = evt.ctrlKey;
  const shift = evt.shiftKey;


  if (!ctrl && !shift && escape && app.confirmSpeedrunReset) {
    app.confirmSpeedrunReset = false;
  }

  if (ctrl && shift && space) {
    if (!app.confirmSpeedrunReset) {
      app.confirmSpeedrunReset = true;
    } else {
      app.speedrunReset();
    }
  }
});

